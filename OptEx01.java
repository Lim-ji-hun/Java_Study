

//산술연산자 -  +,  -,  *(곱), /(나누기). %(나머지)


public class OptEx01 {
	
	public static void main(String[] args) {
		
		int ia = 10, ib = 3;
		System.out.println("ia : " + ia + " -ib : " + ib );
		System.out.println();
		
		System.out.println(ia + "+" + ib + "=" + (ia + ib));
		System.out.println(ia + "-" + ib + "=" + (ia - ib));
		System.out.println(ia + "*" + ib + "=" + (ia * ib));
		System.out.println(ia + "/" + ib + "=" + (ia / ib));
		System.out.println(ia + "%" + ib + "=" + (ia % ib));

	}
}
