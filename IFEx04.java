package ch01_if;

public class IFEx04 {
	public static void main(String[] args) {
		
		int data = 7;
		System.out.println("data : " + data);
		
		if(data >= 0) {
			System.out.println("plus");
		}
		if(data < 0) {
			System.out.println("minus");
		}
		if(data >= 1 && data <= 10) {
			System.out.println("1 ~ 10");
		}
		
		
		
		
		
	}

}
