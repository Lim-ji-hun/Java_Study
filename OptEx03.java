/*
 * 비교연산자
 * <, >, <=, =>
 * == : 같을때가 참
 * != : 다를때가 참
 * - 조건식의 따른 결과는 true or false 이다
 */
public class OptEx03 {
	public static void main(String[] args) {
		
		int ie = 10, ig = 7;
		System.out.println("ie : " + ie + " - ig : " + ig);
		System.out.println();
		
		System.out.println("ie >= ig : " + (ie >= ig));
		System.out.println("ie == ig : " + (ie == ig));
		System.out.println("ie != ig : " + (ie != ig));
		
		
	}
}
